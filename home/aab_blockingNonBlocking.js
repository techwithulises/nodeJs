/*
Blocking vs Non-Blocking:
Relacion que existe en como node.js va a ejecutar las operaciones de entrada y salida.

Sincrono o Bloqueante:
- Se ejecuta una operacion de entrada y salida.
- Se espera a que se termine de ejecutar la operacion de entrada y salida.
- Se continua con la ejecucion del programa.

Asincrono o No Bloqueante:
- Se ejecuta una operacion de entrada y salida.
- Se continua con la ejecucion del programa.
- Cuando la operacion de entrada y salida termina, se ejecuta una funcion de callback.
*/